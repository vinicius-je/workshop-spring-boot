        User ad = new User(null, "admin", "admin@gmail.com", "988888888", passwordEncoder().encode("admin123"));
        ad.setRoles(adminRole);

        User u1 = new User(null, "Maria Brown", "maria@gmail.com", "988888888", passwordEncoder().encode("123123"));
        u1.setRoles(clientRole);

